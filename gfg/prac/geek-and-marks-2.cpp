void solve() {
    int n;
    cin >> n;
    vector<int> a(n);
    for(int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(begin(a), end(a));
    int q;
    cin >> q;
    while(q--) {
        int x;
        cin >> x;
        int pos = -1;
        int l = 0, h = n-1;
        while(l <= h) {
            int m = l + (h-l) / 2;
            if(a[m] <= x) {
                pos = m;
                l = m+1;
            }
            else
                h = m-1;
        }
        int got = 0;
        if(pos < n) {
            if(pos < 0)
                got = n;
            else
                got = n -1 - pos;
        }
        cout << got << '\n';
    }
}

