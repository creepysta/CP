void solve() {
    int n, m;
    cin >> n >> m;
    vector<int> coins(m);
    for(int &i : coins)
        cin >> i;
    vector<int> dp(n+1);
    sort(coins.begin(), coins.end());
    dp[0] = 1;
    for(int i = 0; i < m; i++) {
        for(int j = coins[i]; j <= n; j++) {
            dp[j] += dp[j-coins[i]];
        }
    }
    cout << dp[n] << '\n';
}

